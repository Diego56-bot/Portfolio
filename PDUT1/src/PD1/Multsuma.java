package PD1;

public class Multsuma {
    public static void multsuma(double a, double b, double c) {
        System.out.println(a * b + c);
    }

    public static void main(String[] args) {
        multsuma(1.0, 2.0, 3.0);  // imprime: 5.0
        multsuma(4.0, 5.0, 6.0);  // imprime: 26.0
    }
}

